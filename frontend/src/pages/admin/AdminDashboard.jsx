import React, { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import axios from 'axios';
import Swal from 'sweetalert2';
import { io } from 'socket.io-client'; // ðŸ”‘ Added Socket Client
import Sidebar from '../../components/Sidebar';
import Footer from '../../components/Footer';
import { 
  BarChart3, Users, Settings, ClipboardList, UserPlus, 
  ArrowUpRight, ShieldCheck, Activity, Tv, Share2, Copy, Check, RefreshCw, FileSpreadsheet
} from 'lucide-react';
const API_URL = import.meta.env.VITE_API_URL;

// Initialize socket
const socket = io('http://localhost:5000');

const AdminDashboard = () => {
  const navigate = useNavigate();
  const [copied, setCopied] = useState(false);
  const [stats, setStats] = useState({
    todayVisits: 0,
    activeDoctors: 0,
    avgWait: 0,
    newPatients: 0
  });
  const [loading, setLoading] = useState(true);
  
  const token = localStorage.getItem('token');
  const adminName = localStorage.getItem('userName') || 'Admin';
  const clinicName = localStorage.getItem('clinicName') || 'Your Clinic';
  const clinicCode = localStorage.getItem('clinicCode') || 'CITY01';
  const clinicId = localStorage.getItem('clinicId');

  // The public URL for sharing
  const publicDisplayUrl = `${window.location.origin}/display/${clinicCode}`;

  const fetchLiveStats = async () => {
    try {
      // We fetch live queue to calculate stats
      const res = await axios.get('http://localhost:5000/api/queue/live', {
        headers: { Authorization: `Bearer ${token}` }
      });
      const staffRes = await axios.get('http://localhost:5000/api/staff/all', {
        headers: { Authorization: `Bearer ${token}` }
      });

      const queueData = res.data.data;
      const staffData = staffRes.data.staff;

      setStats({
        todayVisits: queueData.length,
        activeDoctors: staffData.filter(s => s.role === 'doctor' && s.isAvailable).length,
        avgWait: queueData.length * 10, // Simple heuristic: 10 mins per patient
        newPatients: queueData.filter(p => p.visitType === 'Walk-in').length
      });
      setLoading(false);
    } catch (err) {
      console.error("Failed to sync admin stats");
      setLoading(false);
    }
  };

  // ðŸ”Œ WebSocket Lifecycle
  useEffect(() => {
    fetchLiveStats();

    if (clinicId) {
      socket.emit('joinClinic', clinicId);

      // ðŸ“¢ Listen for any queue or staff changes to refresh stats
      socket.on('queueUpdate', fetchLiveStats);
      socket.on('doctorStatusChanged', fetchLiveStats);
      socket.on('staffListUpdated', fetchLiveStats);
    }

    return () => {
      socket.off('queueUpdate');
      socket.off('doctorStatusChanged');
      socket.off('staffListUpdated');
    };
  }, [token, clinicId]);

  const handleShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title: `${clinicName} - Live Queue`,
          text: `View the live consultation queue for ${clinicName}`,
          url: publicDisplayUrl,
        });
      } catch (err) { console.log("Share cancelled"); }
    } else {
      navigator.clipboard.writeText(publicDisplayUrl);
      setCopied(true);
      Swal.fire({
        toast: true,
        position: 'top-end',
        icon: 'success',
        title: 'Display link copied',
        showConfirmButton: false,
        timer: 2000,
        background: '#FFFBF5'
      });
      setTimeout(() => setCopied(false), 3000);
    }
  };

  const modules = [
    {
      title: 'Live TV Display',
      desc: 'Launch the public-facing queue monitor for waiting room TVs.',
      icon: <Tv size={32} color="#FFA800" />,
      link: `/display/${clinicCode}`,
      bgColor: 'bg-[#FFA800]/10',
      isExternal: true 
    },
    {
      title: 'Clinical Reports',
      desc: 'Download clinical data, staff logs, and visit summaries.',
      icon: <FileSpreadsheet size={32} color="#422D0B" />,
      link: '/Admin/reports',
      bgColor: 'bg-[#422D0B]/10'
    },
    {
      title: 'Staff Management',
      desc: 'Add doctors, manage roles, and monitor duty status.',
      icon: <Users size={32} color="#967A53" />,
      link: '/admin/staff-management',
      bgColor: 'bg-[#967A53]/10'
    },
    {
      title: 'Medical History',
      desc: 'Access past consultation records and digital summaries.',
      icon: <ClipboardList size={32} color="#FFA800" />,
      link: '/admin/history',
      bgColor: 'bg-[#FFA800]/10'
    }
  ];

  return (
    <div className="flex min-h-screen bg-[#FFFBF5] font-body text-[#422D0B]">
      <Sidebar role="admin" />

      <div className="flex-grow flex flex-col h-screen overflow-y-auto">
        <main className="p-8 lg:p-12 max-w-6xl mx-auto w-full flex-grow">
          
          <header className="mb-12 flex flex-col md:flex-row justify-between items-start md:items-center gap-6">
            <div>
              <h1 className="text-5xl font-heading mb-2">Namaste, {adminName}</h1>
              <p className="text-[#967A53] font-medium flex items-center gap-2">
                Central Command for <span className="text-[#FFA800] font-bold">{clinicName}</span> 
                <span className="bg-[#E8DDCB] px-2 py-0.5 rounded text-[10px] text-[#422D0B] font-black">{clinicCode}</span>
              </p>
            </div>

            <button 
              onClick={handleShare}
              className="bg-white border border-[#E8DDCB] p-4 rounded-2xl flex items-center gap-4 shadow-sm hover:border-[#FFA800] transition-all group"
            >
              <div className="w-10 h-10 bg-[#422D0B] rounded-full flex items-center justify-center text-white group-hover:bg-[#FFA800] transition-colors">
                {copied ? <Check size={18} /> : <Share2 size={18} />}
              </div>
              <div className="text-left">
                <p className="text-[8px] font-black uppercase text-[#967A53]">Public Monitor</p>
                <p className="text-xs font-bold text-[#422D0B]">Share Live Queue</p>
              </div>
            </button>
          </header>

          <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mb-10">
              <StatMini label="Today's Visits" value={loading ? <RefreshCw size={14} className="animate-spin"/> : stats.todayVisits} />
              <StatMini label="Active Doctors" value={loading ? <RefreshCw size={14} className="animate-spin"/> : stats.activeDoctors} />
              <StatMini label="Queue Avg" value={loading ? "..." : `${stats.avgWait} mins`} />
              <StatMini label="Walk-ins" value={loading ? "..." : stats.newPatients} />
          </div>

          <div className="grid grid-cols-1 md:grid-cols-2 gap-8 mb-12">
            {modules.map((m, idx) => (
              <button
                key={idx}
                onClick={() => m.isExternal ? window.open(m.link, '_blank') : navigate(m.link)}
                className="group bg-white border border-[#E8DDCB] p-10 rounded-[3rem] text-left transition-all hover:shadow-2xl hover:border-[#FFA800] flex items-center gap-8 relative overflow-hidden"
              >
                <div className={`shrink-0 w-20 h-20 ${m.bgColor} rounded-[2rem] flex items-center justify-center group-hover:scale-110 transition-transform`}>
                  {m.icon}
                </div>
                <div>
                  <h3 className="font-heading text-2xl mb-2">{m.title}</h3>
                  <p className="text-sm text-[#967A53] font-medium leading-relaxed">{m.desc}</p>
                </div>
                <ArrowUpRight className="absolute top-8 right-8 text-[#E8DDCB] group-hover:text-[#FFA800] transition-colors" size={24} />
              </button>
            ))}
          </div>

          <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
            <QuickLink title="Duty Roster" icon={<UserPlus size={18}/>} onClick={() => navigate('/admin/staff-management')} />
            <QuickLink title="Reception Desk" icon={<Users size={18}/>} onClick={() => navigate('/receptionist/dashboard')} />
            <QuickLink title="Clinic Settings" icon={<Settings size={18}/>} onClick={() => navigate('/admin/settings')} />
          </div>
        </main>
        <Footer />
      </div>
    </div>
  );
};

const StatMini = ({ label, value }) => (
  <div className="bg-white border border-[#E8DDCB] p-5 rounded-3xl flex flex-col justify-center min-h-[100px]">
    <p className="text-[9px] font-black uppercase text-[#967A53] tracking-widest mb-1">{label}</p>
    <div className="text-2xl font-heading text-[#422D0B]">{value}</div>
  </div>
);

const QuickLink = ({ title, icon, onClick }) => (
  <button 
    onClick={onClick}
    className="flex items-center justify-center gap-3 py-5 px-6 bg-white border border-[#E8DDCB] rounded-2xl text-[10px] font-black uppercase tracking-widest text-[#422D0B] hover:bg-[#FFA800] hover:text-white hover:border-[#FFA800] transition-all shadow-sm active:scale-95"
  >
    {icon} {title}
  </button>
);

export default AdminDashboard;